package B3;
public class Car {
    private String engine;
    private int seats;
    private String color;

    Car() {}

    public String getEngine() {
        return engine;
    }

    public int getSeats() {
        return seats;
    }

    public String getColor() {
        return color;
    }

    void setEngine(String engine) {
        this.engine = engine;
    }

    void setSeats(int seats) {
        this.seats = seats;
    }

    void setColor(String color) {
        this.color = color;
    }


}
